public class switchStmt {
    public static void main(String[] args) {
        int number = 21 ;

        switch (number) {
            case 1: System.out.println("Samosa");
                 break;
            case 2 : System.out.println("Burgar");
                  break;
            case 3 : System.out.println("Pizza");
                  break;
            default: System.out.println("nothing");
            
        }
    }
}
